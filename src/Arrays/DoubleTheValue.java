package Arrays;

public class DoubleTheValue {
    public static void main(String[] args) {
        int salaries[]  = {30,0,50,60,70};
        // doubling the salary
        for (int i = 0; i < salaries.length; i++)
        {
            System.out.println(salaries[i]*2); //60 0 100 120 140
        }
    }
}
