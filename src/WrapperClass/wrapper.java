package WrapperClass;

public class wrapper
{
    public static void main(String[] args)
    {
        System.out.println("Wrapper Class is used to convert primitive data type into objects");
        System.out.println("Remember - string is not a primitive data type");
        System.out.println("wrapper starts with capital letter");

        System.out.println("");
        System.out.println("**********************");
        System.out.println("");

        System.out.println("Primitive & Wrapper diff are given below");
        System.out.println("Primitive  \t Wrapper");
        System.out.println(" char      \t Character");
        System.out.println(" byte      \t Byte");
        System.out.println(" short     \t Short");
        System.out.println(" int       \t Integer");
        System.out.println(" long      \t Long");
        System.out.println(" float     \t Float");
        System.out.println(" double    \t Double");
        System.out.println(" boolean   \t Boolean");


    }
}
