package ForLoop;

public class prog3 {
    public static void main(String[] args) {

        System.out.println("Printing 1st for loop");
        for (int i = 1; i <=10; i=i+2)
        {
            System.out.print(i +" "); // 1 3 5 7 9
        }
        System.out.println();

        System.out.println("Printing 2nd for loop");
        for (int j = 1; j <=10; j=j*2)
        {
            System.out.print(j +" "); // 1 2 4 8
        }
        System.out.println();

    }
}
