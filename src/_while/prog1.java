package _while;

public class prog1 {
    public static void main(String[] args) {
        int i = 1;
        while (i<=5)
        {  System.out.println("i value is : "+i);  // prints 1 to 5
            i++;
        }

        int j=5;
        while (j>=1)
        {  System.out.println("j value is : "+j);  // prints 5 to 1
            j--;
        }
    }
}
